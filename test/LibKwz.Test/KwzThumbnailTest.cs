    [Fact]
    public void TestKwzThumbnail_Read()
    {
        var thumbnail = KwzThumbnail.ReadFrom(new BinaryReader(new MemoryStream(sampleThumbnail)));
        
        var crc32 = System.IO.Hashing.Crc32.HashToUInt32(thumbnail.Data);
        Assert.Equal(thumbnail.Crc32, crc32);
    }

    [Fact]
    public void TestKwzThumbnail_Write()
    {
        var thumbnail = KwzThumbnail.ReadFrom(new BinaryReader(new MemoryStream(sampleThumbnail)));
        var ms = new MemoryStream();
        thumbnail.WriteTo(new BinaryWriter(ms));
        var newThumbnail = KwzThumbnail.ReadFrom(new BinaryReader(new MemoryStream(ms.ToArray())));
        
        Assert.Equal(thumbnail.Crc32, newThumbnail.Crc32);
        Assert.Equal(thumbnail.Data, newThumbnail.Data);
    }
